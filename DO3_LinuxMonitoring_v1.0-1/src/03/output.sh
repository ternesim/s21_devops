#!/bin/bash

#COLORS=(WHITE RED GREEN BLUE BLACK)
COLORS=(39 97 31 32 34 35 30)
BKG_COLORS=(49 107 101 102 44 45 40)

COLOR_TEXT () {
  TEXT_COLOR_KEY=${COLORS["$2"]}
  BKG_COLOR_KEY=${BKG_COLORS["$1"]}
  TEXT_COLOR_VALUE=${COLORS["$5"]}
  BKG_COLOR_VALUE=${BKG_COLORS["$4"]}
  echo -e "\033[0;"$BKG_COLOR_KEY";"$TEXT_COLOR_KEY"m"$3"\033[0;49m = \033[0;"$BKG_COLOR_VALUE";"$TEXT_COLOR_VALUE"m"$6"\033[0;49m"
}

COLOR_TEXT $1 $2 HOSTNAME $3 $4 $HOSTNAME
COLOR_TEXT $1 $2 TIMEZONE $3 $4 $TIMEZONE
COLOR_TEXT $1 $2 USER $3 $4 $USER 
COLOR_TEXT $1 $2 OS $3 $4 "$OS"
COLOR_TEXT $1 $2 DATE $3 $4 "$DATE"
COLOR_TEXT $1 $2 UPTIME $3 $4 "$UPTIME"
COLOR_TEXT $1 $2 UPTIME_SEC $3 $4 $UPTIME_SEC
COLOR_TEXT $1 $2 IP $3 $4 $IP
COLOR_TEXT $1 $2 MASK $3 $4 $MASK
COLOR_TEXT $1 $2 GATEWAY $3 $4 $GATEWAY
COLOR_TEXT $1 $2 RAM_TOTAL $3 $4 "$RAM_TOTAL GB" 
COLOR_TEXT $1 $2 RAM_USED $3 $4 "$RAM_USED GB" 
COLOR_TEXT $1 $2 RAM_FREE $3 $4 "$RAM_FREE GB" 
COLOR_TEXT $1 $2 SPACE_ROOT $3 $4 "$SPACE_ROOT MB" 
COLOR_TEXT $1 $2 SPACE_ROOT_USED $3 $4 "$SPACE_ROOT_USED MB" 
COLOR_TEXT $1 $2 SPACE_ROOT_FREE $3 $4 "$SPACE_ROOT_FREE MB" 